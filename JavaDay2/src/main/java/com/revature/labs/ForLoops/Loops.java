package com.revature.labs.ForLoops;

public class Loops {
	
	public static void main(String[] args) {
		
		/*
		 * for (declaration statement; condition statement; increment statement) {
		 * 		enclosed statements
		 * }
		 * 
		 * Declaration statement = declare a variable ( int i = 0 )
		 * 
		 * Condition statement = checked during each iteration ( i < 10 )
		 * 
		 * Increment statement = executed at the end of each iteration of the for-loop ( i++ ) 
		 * */
		
		// create a simple for-loop that prints the numbers 1 to 10
		for (int i = 0; i <= 10; i++) {
			System.out.println(i);
		}
	}

}
